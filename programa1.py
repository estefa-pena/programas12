 #Programa para verificar si un caracter está en mayúsculas o en minúscula.
Letra = input("Ingrese un carácter: ")
if 'a' <= Letra <= 'z':
    print(f"'{Letra}' está en minúsculas.")
elif 'A' <= Letra <= 'Z':
    print(f"'{Letra}' está en mayúsculas.")
else:
    print(f"'{Letra}' no es una letra.")




  
